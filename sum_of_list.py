lst = [8,2,3,0,7]
sum = 0
for L in lst :
    sum = L + sum 
print(sum)

# explanation :-
# The given sample list is (8, 2, 3, 0, 7), and the expected output is 20.
# To calculate the output, we need to sum all the numbers in the list.
# 8 + 2 + 3 + 0 + 7 = 20
# Therefore, the expected output is 20.